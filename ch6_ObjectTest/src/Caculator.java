

public class Caculator {
	

	
	  int add(int a, int b) {
		int result = a + b;
		
		return result;
	}

	 int subtract(int a, int b) {
			int result = a - b;
			
			return result;
		}
	 
	 int multiply(int a, int b) {
		 int result = a * b;
		 
		 return result;
	 }
	 
	 double divide(int a, int b) {
		 double result = (double)a / b;
		 
		 return result;
	 }

}

